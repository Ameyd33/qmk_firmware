void raw_hid_receive(uint8_t *data, uint8_t length) {
    if (data[1] == 0x01) {
        uint8_t vol_byte = data[2]; // 0-255
        
        // Convert to percentage for display
        uint8_t percent = (vol_byte * 100) / 255;
        
        oled_clear();
        oled_set_cursor(0, 0);
        oled_write_P("PC VOLUME", false);
        
        // Draw a visual bar (Example: 1 pixel height or ASCII)
        oled_set_cursor(0, 1);
        for(uint8_t i=0; i < (percent/10); i++) {
            oled_write_P(PSTR("#"), false); // Filled blocks
        }
        
        oled_set_cursor(12, 1);
        char buf[5];
        itoa(percent, buf, 10);
        oled_write(buf, false);
        oled_write_P(PSTR("%"), false);
    }
}
/* =======================
 * Encoder logic
 * =======================*/
bool encoder_update_user(uint8_t index, bool clockwise) {

    if (menu_active) {
        // Scroll through layers
        if (clockwise) {
            menu_index = (menu_index + 1) % _NUM_LAYERS;
        } else {
            menu_index = (menu_index + _NUM_LAYERS - 1) % _NUM_LAYERS;
        }
        oled_clear();
        enc_layer = menu_index;
        return false;
    }

    // Normal encoder behavior
    switch (current_layer) {
        case _BASE:
            tap_code(clockwise ? KC_VOLU : KC_VOLD);
            break;
        case _MEDIA:
             tap_code(clockwise ? KC_VOLU : KC_VOLD);
            break;
        case _FN:
            tap_code(clockwise ? KC_PGDN : KC_PGUP);
            break;
        case _APPS:
            tap_code(clockwise ? KC_MNXT : KC_MPRV);
            break;
       
    }

    return false;
}


