/* =======================
 * MENU key logic
 * =======================*/
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (!record->event.pressed) return true;
 
     switch (keycode) {

        case MENU_KEY: 
        if (!menu_active) {
            // Enter menu
            menu_active = true;
            menu_index = current_layer;
            oled_clear();
        } else {
            // Confirm layer
            menu_active = false;
            current_layer = menu_index;
            layer_move(current_layer);
            oled_clear();
        }
        return false;

        // Open Chrome + YouTube
        case CHROME_YT:
            tap_code16(LGUI(KC_R));   // Win + R
            wait_ms(300);
            SEND_STRING("chrome youtubemusic.com");
            tap_code(KC_ENTER);
            return false;

        // Open Spotify
        case OPEN_SPOTIFY:
            tap_code16(LGUI(KC_R));
            wait_ms(300);
            SEND_STRING("chrome spotify.com");
            tap_code(KC_ENTER);
            return false;

        // Open specific folder (CHANGE PATH)
        case OPEN_FOLDER:
            tap_code16(LGUI(KC_R));
            wait_ms(300);
            SEND_STRING("\"C:\\\\Users\\\\Amey\\\\Music\"");
            tap_code(KC_ENTER);
            return false;

        // Open Windows Sound / Audio settings
        case AUDIO_SETTINGS:
            tap_code16(LGUI(KC_R));
            wait_ms(300);
            SEND_STRING("ms-settings:sound");
            tap_code(KC_ENTER);
            return false;
    }

    return true;
}

